const express = require('express');

const { getAllPatients, createPatient, updatePatient, deletePatient, getSinglePatient } = require('../controllers/patients');
const checkAuth = require('../middlewares/check-auth')

const router = express.Router();

router.get('/', checkAuth, getAllPatients)
router.post('/', checkAuth, createPatient)
router.put('/:id', checkAuth, updatePatient)
router.delete('/:id', checkAuth, deletePatient)
router.get('/:id', checkAuth, getSinglePatient)

module.exports = router